package exam01;

public class Ex04 {
    public static void main(String[] args) {
        int floor = 3;
        switch(floor) {
            case 1:
                System.out.println("1약국");
                break;
            case 2:
                System.out.println("2정형외과");
                break;
            case 3:
                System.out.println("3피부과");
                break;
            case 4:
                System.out.println("4치과");
                break;
            case 5:
                System.out.println("5층핼스클럽");
                break;
            default:
                System.out.println("없는 층");
        }
    }
}

